package p2;


// Find out square root of number
public class Square_root_17 {

	public static void main(String[] args)
	{
            int num=25;
            double num1= Math.sqrt(num);
            System.out.println("Square root of "+num+" is : "+num1);
            System.out.println("---------------------------------------------------------");
            double num2 = Math.pow(num,0.5);
            System.out.println("Square root of "+num+" is : "+num2);
	}

}
